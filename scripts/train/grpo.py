# --------------------------------------------------------------------------- #
# Collective-communication helpers
# All five functions MUST be called in the same order on both ranks.
# --------------------------------------------------------------------------- #
CTRL_ROLLOUT    = 0
CTRL_SIM_ASSIST = 1
CTRL_SIM_DONE   = 2
CTRL_SYNC       = 3
CTRL_DONE       = 4


def bcast_ctrl(value: int = 0, src: int = 0) -> int:
    """Broadcast a single int from `src` to all ranks. Returns received value."""
    t = torch.tensor([value], dtype=torch.long, device="cuda")
    dist.broadcast(t, src=src)
    return int(t.item())


def bcast_obj(obj, src: int = 0):
    """Broadcast any picklable Python object from `src` to all ranks."""
    buf = [obj if dist.get_rank() == src else None]
    dist.broadcast_object_list(buf, src=src)
    return buf[0]


