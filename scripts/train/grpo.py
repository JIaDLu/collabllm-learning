#!/usr/bin/env python3
"""
Custom GRPO — 2x RTX 4090, full fine-tune (no LoRA), vLLM rollout.

Role separation
───────────────
  Rank 0  (cuda:0)  Training master
    • policy model + frozen reference model
    • AdamW optimizer, GRPO loss, mini-batch gradient update
    • reward computation (runs simulation, scores metrics)
    • W&B logging, HF checkpointing

  Rank 1  (cuda:1)  vLLM rollout worker
    • vLLM engine ONLY — no gradients, no optimizer, no W&B
    • Handles ALL generation: initial rollout AND every assistant turn
      inside the multi-turn simulation. This guarantees every turn in
      every conversation is generated by the same vLLM snapshot,
      eliminating the model-mismatch bug of mixing policy/vLLM turns.

Control protocol (5 signals, always called in identical order on both ranks)
─────────────────────────────────────────────────────────────────────────────
  CTRL_ROLLOUT    (0)  Rank 1 generates K initial completions,
                        then immediately enters the simulation sub-loop.
  CTRL_SIM_ASSIST (1)  Rank 1 generates one assistant-turn batch
                        (called once per turn level, for all active sessions).
  CTRL_SIM_DONE   (2)  Rank 1 exits the simulation sub-loop and returns
                        to the outer loop, ready for the next main signal.
  CTRL_SYNC       (3)  Rank 1 destroys and recreates the vLLM engine from
                        an HF checkpoint written by rank 0.
  CTRL_DONE       (4)  Rank 1 exits its main loop and shuts down.

Weight sync uses policy.save_pretrained() (HF format); vLLM loads from HF dirs.

DataLoader / batching
─────────────────────
  train_batch_size (B)  prompts per outer step
  K                     vLLM completions per prompt  (group size for GRPO)
  mini_batch_size  (MB) (prompt, completion) pairs per gradient sub-step

  Rewards are computed over all B×K pairs. The policy gradient update is then
  applied in MB-sized mini-batches (simple gradient accumulation over B×K/MB
  sub-steps), avoiding a single forward pass over all B×K sequences at once.

Launch
──────
  export WANDB_API_KEY="<key>"
  CUDA_VISIBLE_DEVICES=0,1 \\
  torchrun --master_port=29500 --nproc_per_node=2 -m scripts.train.grpo \\
    --dataset_repo  collabllm/collabllm-multiturn-math-hard \\
    --dataset_name  math-hard \\
    --metric_names  accuracy token_amount \\
    --metric_weights 1 -0.5 \\
    --output_dir    outputs/grpo/qwen2.5-0.5b-math \\
    --wandb_entity  <entity> --wandb_project collabllm \\
    2>&1 | tee grpo.log
"""

